Numerical_Grade = float(input("Enter the student's numerical grade: "))

if Numerical_Grade >= 90.0:
    print("  The student receives a grade of A")
elif Numerical_Grade >=  80.0:
    print("  The student receives a grade of B")
elif Numerical_Grade >=  70.0:
    print("  The student receives a grade of C")
elif Numerical_Grade >=  60.0:
    print("  The student receives a grade of D")
elif Numerical_Grade >=  50.0:
    print("  The student receives a grade of E")
elif Numerical_Grade <= 0.0:
    print("  The student receives a grade of G")
else:
    print("  The student receives a grade of F")
