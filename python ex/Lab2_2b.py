# To calculate the sum of 300.0 + 301.0 + 302.0 + ... + 1000.0

n = 701    # the number of terms
a = 300.0    # the frist number
d = 1.0    # the difference between each number

sum = (n / 2) * (2 * a + (n - 1) * d)

print("The sum the nimbers from 300.0 to 1000.0 is ", sum)
